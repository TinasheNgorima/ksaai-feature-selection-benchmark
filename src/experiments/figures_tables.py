def table_48configs():
    """Print LaTeX for Table 3 (best result per method at each threshold)."""
    path = EXP1_DIR / "experiment_summary.csv"
    if not path.exists():
        print(f"[SKIP] {path} not found — run 01_experiment1_48configs.py first.")
        return

    df = pd.read_csv(path)
    # Best test R² per (method, threshold)
    best = (df.sort_values("test_r2", ascending=False)
              .groupby(["method", "threshold_pct"])
              .first()
              .reset_index())

    print("\n" + "=" * 60)
    print("TABLE 3 — 48-configuration summary (best model per cell)")
    print("=" * 60)
    print(r"\begin{table}[htbp]")
    print(r"\centering")
    print(r"\caption{Best test performance per method and threshold.}")
    print(r"\label{tab:overall_results}")
    print(r"\begin{tabular}{llccc}")
    print(r"\toprule")
    print(r"Method & Threshold & $R^2$ & RMSE & MAE \\")
    print(r"\midrule")
    for _, row in best.sort_values(["method", "threshold_pct"]).iterrows():
        m = row["method"].upper()
        t = f"{row['threshold_pct']:2d}\\%"
        print(f"{m} & {t} & {row['test_r2']:.4f} & {row['test_rmse']:.3f} & {row['test_mae']:.3f} \\\\")
    print(r"\bottomrule")
    print(r"\end{tabular}")
    print(r"\end{table}")


