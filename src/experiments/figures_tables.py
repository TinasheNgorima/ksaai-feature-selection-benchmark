# Figure 2 – Performance–Stability Pareto frontier
# =============================================================================

def fig_pareto():
    """
    Plot Figure 2: mean R² vs CoV(R²)% across 30 repetitions for each method.
    Data source: results/stability/aggregated_summary.csv
    """
    agg = pd.read_csv(STABILITY_DIR / "aggregated_summary.csv")

    # Build summary dict
    data = {
        row["Method"]: {"mean_r2": row["Mean_R2"], "cv_r2": row["CV_R2_pct"]}
        for _, row in agg.iterrows()
    }

    plt.rcParams.update(JMLR_RC)
    fig, ax = plt.subplots(figsize=(3.5, 2.8))

    pts = np.array([[v["mean_r2"], v["cv_r2"]] for v in data.values()])
    methods = list(data.keys())

    # Scatter
    for i, m in enumerate(methods):
        ax.scatter(
            data[m]["mean_r2"], data[m]["cv_r2"],
            s=60, color=METHOD_COLORS.get(m, "grey"),
            edgecolors="black", linewidth=0.5, zorder=5,
            label=METHOD_LABELS.get(m, m),
        )

    # Pareto frontier (maximise R², minimise CV) via convex hull
    inv = pts.copy()
    inv[:, 1] = -inv[:, 1]
    try:
        hull = ConvexHull(inv)
        pareto_pts = pts[hull.vertices]
        pareto_pts = pareto_pts[pareto_pts[:, 0].argsort()]
        ax.plot(pareto_pts[:, 0], pareto_pts[:, 1],
                color="#d62728", linestyle="--", linewidth=1.2, alpha=0.9,
                label="Pareto frontier")
    except Exception:
        pass  # ConvexHull requires ≥ 4 non-degenerate points

    # Annotations
    for m in methods:
        ax.annotate(
            METHOD_LABELS.get(m, m),
            (data[m]["mean_r2"], data[m]["cv_r2"]),
            xytext=(3, 3), textcoords="offset points", fontsize=9,
        )

    ax.set_xlabel(r"Mean $R^2$ (Performance)")
    ax.set_ylabel(r"CV($R^2$)% (Instability)")
    ax.grid(True, linestyle=":", alpha=0.5, linewidth=0.5)
    ax.legend(frameon=True, fancybox=False, edgecolor="black",
              facecolor="white", framealpha=0.9)

    # Shaded "optimal" region (high R², low CV)
    if len(pts) > 0:
        xmax = pts[:, 0].max()
        ymin = pts[:, 1].min()
        ax.axhspan(ymin - 0.3, ymin + 0.4, xmin=0.7, xmax=1.0,
                   alpha=0.08, color="green", zorder=0)

    plt.tight_layout()
    for ext in ("pdf", "png"):
        fig.savefig(FIGURES_DIR / f"Figure_2_Pareto_frontier.{ext}")
    plt.close(fig)
    print("Saved Figure 2 (Pareto frontier)")


