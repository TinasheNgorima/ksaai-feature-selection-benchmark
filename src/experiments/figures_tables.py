# Statistical-test heatmaps (Nemenyi p-values + Cohen's d)
# =============================================================================

def fig_stat_tests():
    """
    Figure for Section 4.2: two-panel heatmap — Nemenyi post-hoc p-values and
    Cohen's d effect sizes for R² and MAE.

    Values are read directly from the manuscript (Ksaai.tex); update if numbers change.
    """
    methods_labels = ["DC", "MI", "MIC", r"$\xi_n$"]

    # Nemenyi p-value matrices (upper triangle)
    nemenyi = {
        "R²": np.array([
            [np.nan, 0.005, 0.168, 0.0001],
            [0.005,  np.nan, 0.585, 0.168],
            [0.168,  0.585,  np.nan, 0.005],
            [0.0001, 0.168,  0.005,  np.nan],
        ]),
        "MAE": np.array([
            [np.nan, 0.009,  0.036,  0.0001],
            [0.009,  np.nan, 0.965,  0.826],
            [0.036,  0.965,  np.nan, 0.023],
            [0.0001, 0.826,  0.023,  np.nan],
        ]),
    }

    # Cohen's d matrices (placeholder; replace with bootstrap results from 01 output)
    cohens_d = {
        "R²": np.array([
            [np.nan,  0.33,  0.22,  0.09],
            [ 0.33, np.nan, -0.11, -0.24],
            [ 0.22,  0.11, np.nan, -0.13],
            [ 0.09,  0.24,  0.13, np.nan],
        ]),
        "MAE": np.array([
            [np.nan,  0.28,  0.19,  0.12],
            [ 0.28, np.nan, -0.09, -0.16],
            [ 0.19,  0.09, np.nan, -0.07],
            [ 0.12,  0.16,  0.07, np.nan],
        ]),
    }

    plt.rcParams.update(JMLR_RC)

    for metric in ["R²", "MAE"]:
        fig, axes = plt.subplots(1, 2, figsize=(6.50, 2.8))
        metric_safe = metric.replace("²", "2")

        # Left panel: Nemenyi p-values
        ax = axes[0]
        pmat = nemenyi[metric]
        # Mask diagonal
        mask = np.zeros_like(pmat, dtype=bool)
        np.fill_diagonal(mask, True)

        sns.heatmap(
            pmat, ax=ax, mask=mask, annot=True, fmt=".3f",
            cmap="RdYlGn_r", vmin=0, vmax=0.2,
            xticklabels=methods_labels, yticklabels=methods_labels,
            linewidths=0.5, linecolor="white",
            cbar_kws={"label": "p-value", "shrink": 0.8},
        )
        ax.set_title(f"Nemenyi p-values ({metric})", fontsize=10)
        ax.tick_params(axis="x", rotation=0)
        ax.tick_params(axis="y", rotation=0)

        # Right panel: Cohen's d
        ax = axes[1]
        dmat = cohens_d[metric]
        np.fill_diagonal(mask, True)

        sns.heatmap(
            dmat, ax=ax, mask=mask, annot=True, fmt=".2f",
            cmap="coolwarm", center=0, vmin=-0.5, vmax=0.5,
            xticklabels=methods_labels, yticklabels=methods_labels,
            linewidths=0.5, linecolor="white",
            cbar_kws={"label": "Cohen's $d$", "shrink": 0.8},
        )
        ax.set_title(f"Cohen's $d$ ({metric})", fontsize=10)
        ax.tick_params(axis="x", rotation=0)
        ax.tick_params(axis="y", rotation=0)

        plt.tight_layout()
        out = FIGURES_DIR / f"fig_stat_tests_{metric_safe}"
        for ext in ("pdf", "png"):
            fig.savefig(f"{out}.{ext}")
        plt.close(fig)
        print(f"Saved statistical-tests figure ({metric})")


