def table_efficiency():
    """Print LaTeX for Table 6 (timing comparison)."""
    combined_path = EFFICIENCY_DIR / "timing_all.csv"
    if not combined_path.exists():
        combined_path = EFFICIENCY_DIR / "timing_dc_mi_xi.csv"
    if not combined_path.exists():
        print(f"[SKIP] Timing CSV not found — run 03 and 04 first.")
        return

    df = pd.read_csv(combined_path)

    # Reference for speedup: DC
    dc_row = df[df["method"] == "dc"]
    if dc_row.empty:
        print("[SKIP] DC row not found in timing data.")
        return
    dc_time = float(dc_row["mean_total_s"].values[0])

    print("\n" + "=" * 60)
    print("TABLE 6 — Computational efficiency")
    print("=" * 60)
    print(r"\begin{table}[htbp]")
    print(r"\centering")
    print(r"\caption{Computational efficiency comparison (30 repetitions, "
          r"$n = 15{,}542$, $p = 81$). "
          r"Speedup is relative to DC.}")
    print(r"\label{tab:efficiency}")
    print(r"\begin{tabular}{lcccc}")
    print(r"\toprule")
    print(r"Method & Time (s) & s/feature & Speedup vs.\ DC & CV\,\% \\")
    print(r"\midrule")
    for _, row in df.iterrows():
        m_raw = row["method"]
        m = METHOD_LABELS.get(m_raw.upper(), m_raw.upper())
        spd = dc_time / row["mean_total_s"]
        print(f"{m} & {row['mean_total_s']:.2f}\\;$\\pm$\\;{row['std_total_s']:.2f} & "
              f"{row.get('mean_s_per_feat', row.get('mean_s_per_feature', float('nan'))):.4f} & "
              f"{spd:.1f}$\\times$ & {row['cv_pct']:.1f} \\\\")
    print(r"\bottomrule")
    print(r"\end{tabular}")
    print(r"\end{table}")


